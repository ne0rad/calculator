function add() {
    args = [...arguments];
    return args[0] + args[1];
}

function substract() {
    args = [...arguments];
    return args[0] - args[1];
}

function divide() {
    args = [...arguments];
    return args[0] / args[1];
}

function multiply() {
    args = [...arguments];
    return args[0] * args[1];
}